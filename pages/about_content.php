	<h1 id="page_title">About</h1>
	<p>Have you ever listened to a friend talk about the amazing Tabletop RPG game 
	they were playing last night and the phrase &quot;You had to have been there.&quot; 
	comes out of their mouth? Or maybe you have played a game that you wanted to read
	through again but can't because you did not record it, or the posts are gone? The Chronicler
	is here to help!</p>
	<p>The Chronicler is a <a style="text-decoration:underline" href="https://discordapp.com/">Discord</a> bot made for Discord servers that play Tabletop Roleplaying Games like
	D&D or Pathfinder. It records all text messages sent to a server's whitelisted channel into its 
	database so if the Discord server is ever deleted, the Chronicle will remain as long as it does not get blacklisted.
	Anyone can then read the recorded games on this website in a novelized format. Reread your favorite moments
	in your own game, or read through other player's games. Click <a style="text-decoration:underline" href="http://chronicler.seanmfrancis.net/chronicle.php?id=1&page=1">Here</a> 
	to see an example of how the website displays what it has recorded.</p>
	<p>We hope to make The Chronicler grow even more, and we have ideas of how to do that. But we want to make this a project that everyone, not just ourselves, can enjoy. So, if you
	have a comment, suggestion, or issue, please <a style="text-decoration:underline" href="http://chronicler.seanmfrancis.net/contact.php">Contact Us</a>.
	We read all that comes our way and want to do what we can to satisfy all of our users.</p>
